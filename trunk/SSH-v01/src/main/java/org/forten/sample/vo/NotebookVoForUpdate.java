package org.forten.sample.vo;

public class NotebookVoForUpdate {
	private int id;
	private String username;
	private String content;

	public NotebookVoForUpdate() {
		super();
	}

	public NotebookVoForUpdate(int id, String username, String content) {
		super();
		this.id = id;
		this.username = username;
		this.content = content;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	@Override
	public String toString() {
		return "NotebookVoForUpdate [id=" + id + ", username=" + username + ", content=" + content + "]";
	}
}
